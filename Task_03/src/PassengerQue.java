public class PassengerQue {
    String[] Queue = new String[3];
    int front;
    int rear;
    int size;

    //***************************************************************************************************************************

    public void enQueue(String data){
        Queue[rear] = data;
        rear = (rear + 1)%3;
        size = size + 1;
    }

    //***************************************************************************************************************************

    public String deQueue(){
        String data = Queue[front];
        front = (front +1)%3;
        size = size - 1;

        return data;
    }

    //***************************************************************************************************************************

    public boolean isFull(){
        return size == 12;
    }
    //***************************************************************************************************************************

    public boolean isEmpty(){
        return size == 0;
    }
    //***************************************************************************************************************************
}